static int fb_fd = -1;
static char *fbp = NULL;
static struct fb_var_screeninfo vinfo;
static struct fb_fix_screeninfo finfo;

int fb_ac() {
    fb_fd = open("/dev/fb0", O_RDWR);
    if (fb_fd == -1) {
        perror("purrafik: mama kabim dokuldu! (/dev/fb0 acilamadi)");
        return -1;
    }
    if (ioctl(fb_fd, FBIOGET_FSCREENINFO, &finfo) == -1) {
        perror("purrafik: su kabimi doktuler! >:( [finfo bilgisi alinamadi]");
        return -1;
    }
    if (ioctl(fb_fd, FBIOGET_VSCREENINFO, &vinfo) == -1) {
        perror("purrafik: kavga ettim, kaybettim :( [vinfo bilgisi alinamadi]");
        return -1;
    }
    long screensize = finfo.line_length * vinfo.yres;
    fbp = (char *)mmap(0, screensize, PROT_READ | PROT_WRITE, MAP_SHARED, fb_fd, 0);
    if ((intptr_t)fbp == -1) {
        perror("purrafik: dustum D: (mmap basarisiz)");
        return -1;
    }
    return 0;
}
void fb_temizle(unsigned int renk) {
    long screensize = finfo.line_length * vinfo.yres;
    for (long i = 0; i < screensize; i += 4) {
        *((unsigned int *)(fbp + i)) = renk;
    }
}

void fb_kapat() {
    if (fbp) {
        long screensize = finfo.line_length * vinfo.yres;
        munmap(fbp, screensize);
        fbp = NULL;
    }
    if (fb_fd != -1) {
        close(fb_fd);
        fb_fd = -1;
    }
}

static ssfn_t ssfn_ctx;
static ssfn_font_t *ssfn_font_data = NULL;

int ssfn_ac(const char *font_dosya, int boyut) {
    FILE *f = fopen(font_dosya, "rb");
    if (!f) { perror("ssfn: font acilamadi"); return -1; }
    fseek(f, 0, SEEK_END);
    long fsize = ftell(f);
    fseek(f, 0, SEEK_SET);
    ssfn_font_data = (ssfn_font_t *)malloc(fsize);
    fread(ssfn_font_data, 1, fsize, f);
    fclose(f);
    memset(&ssfn_ctx, 0, sizeof(ssfn_t));
    ssfn_load(&ssfn_ctx, ssfn_font_data);
    ssfn_select(&ssfn_ctx, SSFN_FAMILY_ANY, NULL, SSFN_STYLE_REGULAR, boyut);
    return 0;
}

void ssfn_yazi(int x, int y, const char *s, unsigned int renk) {
    ssfn_buf_t buf;
    buf.ptr = (uint8_t *)fbp;
    buf.w = vinfo.xres;
    buf.h = vinfo.yres;
    buf.p = finfo.line_length;
    buf.x = x;
    buf.y = y;
    buf.fg = (0xFF << 24) | renk;
    buf.bg = 0;
    while (*s)
        s += ssfn_render(&ssfn_ctx, &buf, s);
}

void ssfn_kapat() {
    ssfn_free(&ssfn_ctx);
    if (ssfn_font_data) { free(ssfn_font_data); ssfn_font_data = NULL; }
}

void fb_piksel(int x, int y, unsigned int renk) {
    if (x < 0 || x >= vinfo.xres || y < 0 || y >= vinfo.yres) return;
    long konum = (x * (vinfo.bits_per_pixel / 8)) + (y * finfo.line_length);
    if (vinfo.bits_per_pixel == 32) {
        *((unsigned int *)(fbp + konum)) = renk;
    } else if (vinfo.bits_per_pixel == 16) {
        unsigned short b = (renk & 0xFF) >> 3;
        unsigned short g = ((renk >> 8) & 0xFF) >> 2;
        unsigned short r = ((renk >> 16) & 0xFF) >> 3;
        *((unsigned short *)(fbp + konum)) = (r << 11) | (g << 5) | b;
    }
}

void fb_dikdortgen(int x, int y, int w, int h, unsigned int renk) {
    for (int cy = y; cy < y + h; cy++)
        for (int cx = x; cx < x + w; cx++)
            fb_piksel(cx, cy, renk);
}

void fb_karakter(int x, int y, char c, unsigned int renk) {
    if (c < 0 || c > 127) return;
    unsigned char *bitmap = font8x8_basic[(int)c];
    for (int satir = 0; satir < 8; satir++) {
        for (int sutun = 0; sutun < 8; sutun++) {
            if (bitmap[satir] & (1 << sutun)) {
                fb_piksel(x + sutun, y + satir, renk);
            }
        }
    }
}

void fb_yazi(int x, int y, const char *s, unsigned int renk) {
    while (*s) {
        fb_karakter(x, y, *s, renk);
        x += 8;
        s++;
    }
}
