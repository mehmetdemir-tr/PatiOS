#pragma pack(pop)

void fb_bmp(const char *dosya, int x, int y) {
    FILE *f = fopen(dosya, "rb");
    if (!f) return;

    BMPHeader header;
    fread(&header, sizeof(BMPHeader), 1, f);

    if (header.type != 0x4D42) { fclose(f); return; } // "BM" kontrolü

    int genislik = header.width_px;
    int yukseklik = header.height_px < 0 ? -header.height_px : header.height_px;
    int row_stride = (genislik * 3 + 3) & ~3;

    uint8_t *pixel_data = malloc(row_stride * yukseklik);
    fseek(f, header.offset, SEEK_SET);
    fread(pixel_data, row_stride * yukseklik, 1, f);
    fclose(f);

    for (int py = 0; py < yukseklik; py++) {
        uint8_t *src = pixel_data + (yukseklik - 1 - py) * row_stride;
        for (int px = 0; px < genislik; px++) {
            uint8_t b = src[px * 3 + 0];
            uint8_t g = src[px * 3 + 1];
            uint8_t r = src[px * 3 + 2];
            unsigned int renk = (r << 16) | (g << 8) | b;
            fb_piksel(x + px, y + py, renk);
        }
    }
    free(pixel_data);
}

void fb_overlay(int x, int y, int w, int h, unsigned int renk, int alpha) {
    for (int cy = y; cy < y + h; cy++) {
        for (int cx = x; cx < x + w; cx++) {
            if (cx < 0 || cx >= vinfo.xres || cy < 0 || cy >= vinfo.yres) continue;
            long konum = (cx * (vinfo.bits_per_pixel / 8)) + (cy * finfo.line_length);
            unsigned int mevcut = *((unsigned int *)(fbp + konum));
            unsigned int r = ((mevcut >> 16) & 0xFF) * (255 - alpha) / 255 + ((renk >> 16) & 0xFF) * alpha / 255;
            unsigned int g = ((mevcut >> 8) & 0xFF) * (255 - alpha) / 255 + ((renk >> 8) & 0xFF) * alpha / 255;
            unsigned int b = (mevcut & 0xFF) * (255 - alpha) / 255 + (renk & 0xFF) * alpha / 255;
            *((unsigned int *)(fbp + konum)) = (r << 16) | (g << 8) | b;
        }
    }
}

void fb_yazi_buyuk(int x, int y, const char *s, unsigned int renk) {
    while (*s) {
        for (int satir = 0; satir < 8; satir++)
            for (int sutun = 0; sutun < 8; sutun++)
                if (font8x8_basic[(int)*s][satir] & (1 << sutun))
                    fb_dikdortgen(x + sutun * 3, y + satir * 3, 3, 3, renk);
        x += 24;
        s++;
    }
}

